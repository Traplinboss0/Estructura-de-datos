#Metodo append
lista = [1,2,3,4,5]
lista.append(6)
print(lista)

#metodo clear
lista.clear()
print(lista)

#metodo extend
lista1 = [1,2,3]
lista2 = [4,5,6]
lista1.extend(lista2)
print(lista1)

#metodo count
Universo = ["mundo", "tierra", "universo", "planeta","planeta","mundo","planeta","universo","tierra"]
print(Universo.count("planeta"))

#metodo index
Universo = ["mundo", "tierra", "universo", "planeta","planeta","mundo","planeta","universo","tierra"]
print(Universo.index("universo"))

#metodo insert
lis = [1,2,3]
